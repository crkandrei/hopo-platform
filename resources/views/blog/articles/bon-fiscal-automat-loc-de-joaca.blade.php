<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Întrebări frecvente despre bonurile fiscale la locuri de joacă</h2>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Pot emite facturi în loc de bonuri fiscale?</h3>

<p class="text-gray-600 leading-relaxed mb-4">Nu — pentru tranzacțiile directe cu persoane fizice (persoane care nu sunt înregistrate în scop de TVA), este obligatoriu bonul fiscal de la casa de marcat, nu factura. Factura se poate emite suplimentar, la cererea clientului (de exemplu dacă un angajator plătește accesul copilului), dar nu înlocuiește bonul fiscal.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Ce fac dacă imprimanta casei de marcat se defectează?</h3>

<p class="text-gray-600 leading-relaxed mb-4">Tehnic, nu poți continua activitatea fără casă de marcat funcțională. Ai câteva opțiuni: un service rapid prin dealer (multe au intervenție în aceeași zi), o casă de marcat de backup (recomandată pentru locații cu volum mare), sau suspendarea temporară a activității până la remediere. Niciodată nu continua să încasezi fără să poți emite bon fiscal.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Trebuie să emit bon fiscal și pentru abonamente?</h3>

<p class="text-gray-600 leading-relaxed mb-4">Da. Dacă vinzi abonamente lunare sau pachete de intrări, fiecare plată trebuie însoțită de bon fiscal. Singura excepție sunt plățile prin transfer bancar (OP/virament) pentru care emiterea facturii este suficientă — dar și în acest caz trebuie să ai o procedură clară de evidență.</p>

