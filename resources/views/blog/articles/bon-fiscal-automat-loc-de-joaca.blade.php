<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Problema cu bonurile emise manual</h2>

<p class="text-gray-600 leading-relaxed mb-4">Chiar dacă ai casă de marcat, mulți proprietari emit bonurile manual: angajatul introduce suma în casă și apasă print. Pare simplu, dar în practică generează probleme serioase.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Erori de calcul la ore fracționate</h3>

<p class="text-gray-600 leading-relaxed mb-4">Calculul manual al timpului petrecut e cea mai frecventă sursă de erori. Exemplu concret: un copil a intrat la 14:37 și iese la 16:02. Asta înseamnă 1 oră și 25 de minute. Dacă tariful e 25 RON/oră cu rotunjire la 30 de minute, suma corectă e 37,50 RON. Un angajat obosit sau grăbit va calcula greșit de câteva ori pe zi — în favoarea clientului sau în dezavantajul tău.</p>

<p class="text-gray-600 leading-relaxed mb-4">Înmulțit cu 50–100 de copii pe zi, erorile de calcul pot reprezenta 200–500 RON pierderi lunare doar din calculul greșit al timpului. Nu e o sumă mare izolat, dar pe un an înseamnă 2.400–6.000 RON lăsați pe masă.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Presiunea din orele de vârf</h3>

<p class="text-gray-600 leading-relaxed mb-4">Sâmbăta după-amiaza, când ies 8–10 copii simultan, un angajat care calculează și emite bonuri manual creează o coadă la ieșire. Părinții se enervează. Unii pleacă fără să plătească „că era haos". Alții plătesc suma greșit calculată fără să observe. Niciuna din variante nu e bună pentru afacerea ta.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Riscul de neconformitate</h3>

<p class="text-gray-600 leading-relaxed mb-4">Emiterea manuală crește riscul ca suma de pe bon să nu corespundă exact cu suma plătită — fie din eroare de calcul, fie din graba momentului. Dacă ANAF constată că bonul arată 30 RON dar clientul a plătit efectiv 37,50 RON, ai o abatere fiscală chiar dacă ai emis bon.</p>

