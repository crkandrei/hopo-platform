<h1 class="text-4xl font-bold text-gray-900 mb-6">Bon fiscal automat pentru loc de joacă: ce obligații ai față de ANAF în 2026</h1>

<p class="text-gray-600 leading-relaxed mb-4">Dacă ai un loc de joacă indoor în România și nu emiți bon fiscal la fiecare plată, riști amenzi care încep de la 2.000 RON per abatere. Nu e o exagerare — ANAF a intensificat controalele în segmentul serviciilor pentru copii tocmai pentru că multe locații lucrau cu cash fără documente fiscale. În 2026, regulile sunt clare și nu există excepții: orice sumă încasată de la client trebuie să genereze un bon fiscal.</p>

<p class="text-gray-600 leading-relaxed mb-4">Problema cu care se confruntă majoritatea proprietarilor nu e lipsa de intenție de a respecta legea, ci modul în care emit bonurile: manual, cu erori de calcul la ore fracționate, lent în orele de vârf. Acest articol îți explică cum arată obligațiile legale, ce casă de marcat ai nevoie și cum automatizezi procesul complet ca să nu mai pierzi timp și să nu mai riști amenzi.</p>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Este obligatoriu bonul fiscal la locul de joacă?</h2>

<p class="text-gray-600 leading-relaxed mb-4">Da, fără nicio excepție. Conform OUG 28/1999 privind obligația operatorilor economici de a utiliza aparate de marcat electronice fiscale, orice operator economic care prestează servicii cu plată direct către publicul larg este obligat să emită bon fiscal. Locurile de joacă indoor intră în această categorie indiferent de forma juridică (SRL sau PFA), de mărimea locației sau de frecvența tranzacțiilor.</p>

<p class="text-gray-600 leading-relaxed mb-4">Legea nu face distincție între plata cu cash și plata cu cardul — ambele obligă la emiterea bonului fiscal. Singura diferență este că pentru plățile cu cardul există soluții de integrare cu terminalul POS care pot automatiza și mai mult procesul.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Amenzi ANAF pentru neemiterea bonului fiscal</h3>

<p class="text-gray-600 leading-relaxed mb-4">Sancțiunile pentru nerespectarea obligației de emitere a bonului fiscal sunt:</p>

<ul class="list-disc list-inside space-y-2 text-gray-600 mb-6 ml-4">
    <li><strong>Amendă 2.000 – 4.000 RON</strong> pentru neemiterea bonului fiscal sau emiterea unui bon incorect</li>
    <li><strong>Suspendarea activității</strong> pentru 3 luni la a doua constatare</li>
    <li><strong>Confiscarea sumelor</strong> pentru care nu s-au emis bonuri fiscale</li>
    <li><strong>Amendă suplimentară</strong> dacă casa de marcat nu e conectată la serverele ANAF</li>
</ul>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Cum se desfășoară controalele ANAF în locuri de joacă</h3>

<p class="text-gray-600 leading-relaxed mb-4">Inspectorii ANAF intră de regulă ca „clienți obișnuiți" — vin cu un copil sau singuri și plătesc accesul. Dacă nu primesc bon fiscal, sau dacă bonul emis nu corespunde cu suma plătită, sancțiunea e imediată. Nu există perioadă de grație sau avertisment prealabil.</p>

<p class="text-gray-600 leading-relaxed mb-4">Controalele sunt mai frecvente în perioadele cu trafic ridicat: vacanțele școlare, week-end-urile, sărbătorile legale — exact momentele când locurile de joacă sunt cele mai aglomerate și când tentația de a „scurta" procesul e mai mare.</p>

<div class="bg-red-50 border-l-4 border-red-400 p-4 rounded-r-lg mb-6">
    <p class="text-gray-700 font-medium">Atenție: amenda se dă per abatere constatată, nu per zi. Dacă inspectorul constată că 5 clienți nu au primit bon fiscal, poți acumula 5 × 2.000 RON = 10.000 RON amendă dintr-o singură vizită.</p>
</div>

<h2 class="text-2xl font-bold text-gray-900 mt-10 mb-4">Ce casă de marcat îți trebuie la locul de joacă?</h2>

<p class="text-gray-600 leading-relaxed mb-4">Din 2019, toate casele de marcat din România trebuie să fie conectate la serverele ANAF și să transmită datele în timp real. Acest lucru înseamnă că nu poți folosi o casă de marcat veche, neconectată la internet — chiar dacă funcționează tehnic și emite bonuri.</p>

<h3 class="text-xl font-semibold text-gray-800 mt-6 mb-3">Cerințe tehnice obligatorii</h3>

<ul class="list-disc list-inside space-y-2 text-gray-600 mb-6 ml-4">
    <li>Casă de marcat omologată ANAF (apare pe lista oficială ANAF)</li>
    <li>Conectare la internet prin SIM sau Wi-Fi pentru transmisia datelor la ANAF</li>
    <li>Actualizare firmware la zi (producătorul este responsabil de actualizări)</li>
    <li>Jurnal electronic stocat pe suport de memorie fiscal internă</li>
</ul>

